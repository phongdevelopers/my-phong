namespace CommerceBuilder.Products
{
    /// <summary>
    /// Enumeration that represents the type of input field
    /// </summary>
    public enum InputType
    {
        /// <summary>
        /// This is a hidden input field
        /// </summary>
        Hidden,

        /// <summary>
        /// This is a label
        /// </summary>
        Label,

        /// <summary>
        /// This is a text box
        /// </summary>
        TextBox,

        /// <summary>
        /// This is a text area
        /// </summary>
        TextArea,

        /// <summary>
        /// This is a drop down list box
        /// </summary>
        DropDownListBox,

        /// <summary>
        /// This is a list box
        /// </summary>
        ListBox,

        /// <summary>
        /// This is a multiple select list box
        /// </summary>
        MultipleListBox,

        /// <summary>
        /// This is a radio button list
        /// </summary>
        RadioButtonList,

        /// <summary>
        /// This is a check-box list
        /// </summary>
        CheckBoxList
    }
}
